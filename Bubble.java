public class Bubble {
    private String name;
    private String school;
    private String description;

    public Bubble(String name, String school, String description) {
        this.name = name;
        this.school = school;
        this.description = description;
    }

    public String getSchool() {
        return school;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }
}
